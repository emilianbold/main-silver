    public AmazonWizardComponent2(List<Node> servers) {
        this.servers = servers;
        initComponents();
        setName(NbBundle.getBundle(AmazonWizardComponent2.class).getString("LBL_Name2")); // NOI18N
        DefaultListModel m = new DefaultListModel();
        for (Node n : servers) {
            m.addElement(n);
        }
        if (m.isEmpty()) {
            m.addElement(NbBundle.getMessage(AmazonWizardComponent2.class, "AmazonWizardComponent2.none"));
        }
        jList1.setModel(m);
        jList1.setCellRenderer(new ListCellRenderer() {

            @Override
            public Component getListCellRendererComponent(JList list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                if (value instanceof Node) {
                    Node n = (Node)value;
                    return new JLabel(n.getDisplayName(), ImageUtilities.image2Icon(n.getIcon(BeanInfo.ICON_COLOR_16x16)), SwingUtilities.LEADING);
                } else {
                    return new JLabel((String)value);
                }
            }
        });
    }

